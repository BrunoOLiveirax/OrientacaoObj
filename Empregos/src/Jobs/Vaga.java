package Jobs;

public class Vaga {
		private int salario;
		private String cargo;
		private String modeloVaga;
		public int getSalario() {
			return salario;
		}
		public void setSalario(int salario) {
			this.salario = salario;
		}
		public String getCargo() {
			return cargo;
		}
		public void setCargo(String cargo) {
			this.cargo = cargo;
		}
		public String getModeloVaga() {
			return modeloVaga;
		}
		public void setModeloVaga(String modeloVaga) {
			this.modeloVaga = modeloVaga;
		}
	

}
